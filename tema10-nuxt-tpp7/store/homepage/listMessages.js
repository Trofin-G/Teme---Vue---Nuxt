export const mutations = {
  setListMessages(state, message) {
    state.listMessages = message;
  },

  setCloneListMessages(state) {
    state.cloneListMessages = [...state.listMessages];

    // sort
    state.cloneListMessages = state.listMessages.sort(
      (a, b) => b.read - a.read
    );

    // pagination
    state.paginationHandler = [];
    if (state.receivedSentHandler == true) {
      state.paginationHandler = [...state.cloneListMessages];
    }
  },

  setListSentMessages(state, message) {
    state.listSentMessages = message;
  },

  setListSentMessagesNewMess(state, formData) {
    formData.id = state.listSentMessages.length + 1;
    formData.url = state.listSentMessages.length + 1;

    state.listSentMessages.push(formData);
  },

  setMessageUnread(state) {
    state.messageUnread = state.listMessages.filter((item) => {
      return item.read == true;
    });

    state.messageUnread = state.messageUnread.length;
  },

  setMessageDetails(state, valueSlug) {
    let dataArray = state.receivedSentHandler
      ? [...state.cloneListMessages]
      : [...state.listSentMessages];
    state.cloneListMessages = dataArray.filter((item) => {
      return String(item.url) == String(valueSlug);
    });
  },

  setSearchValue(state, searchValue) {
    state.searchMessages = [];
    state.paginationHandler = [];

    if (
      searchValue !== "" ||
      searchValue !== null ||
      searchValue !== undefined
    ) {
      state.cloneListMessages.forEach((item) => {
        if (
          item.subject.toLowerCase().includes(searchValue) ||
          item.message.toLowerCase().includes(searchValue)
        ) {
          state.searchMessages.push(item);
        }
      });
    } else {
      state.searchMessages = state.cloneListMessages;
    }

    state.paginationHandler = [...state.searchMessages];
  },

  setRecivedSentHandler(state, receivedSentHandler) {
    return (state.receivedSentHandler = receivedSentHandler);
  },

  setCurrentPage(state, pageValue) {
    state.totalPagesArray = [];
    state.paginationMessages = [];
    state.currentPage = 1;

    state.currentPage = pageValue;
    let start = state.perPage * (state.currentPage - 1);
    let end = start + state.perPage;

    state.paginationHandler.slice(start, end).forEach((item) => {
      state.paginationMessages.push(item);
    });

    state.totalPages = Math.ceil(
      state.paginationHandler.length / state.perPage
    );

    for (let i = 1; i <= state.totalPages; i++) {
      state.totalPagesArray.push(i);
    }
  },

  setReadValue(state, valueSlug) {
    state.cloneListMessages.find((item) => {
      if (item.id == valueSlug) {
        item.read = false;
      }
    });
  },
};

export const actions = {
  getMessageData({ commit, dispatch, state }, valueSlug) {
    console.log(valueSlug, 'asta');
    commit("setMessageDetails", `${valueSlug}`);
    commit("setReadValue", valueSlug);
  },

  sendSearchValue({ commit, dispatch, state }, searchValue) {
    this.$router.push(`/search/${searchValue}`);
    commit("setSearchValue", searchValue);
  },

  sendRecivedSentHandler({ commit, dispatch, state }, receivedSentHandler) {
    commit("setRecivedSentHandler", receivedSentHandler);
  },

  sendPageValue({ commit, dispatch, state }, pageValue) {
    commit("setCurrentPage", pageValue);
  },

  sendFormData({ commit, dispatch, state }, formData) {
    commit("setListSentMessagesNewMess", formData);
  },
};
